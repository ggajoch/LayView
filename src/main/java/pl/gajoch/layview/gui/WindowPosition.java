package pl.gajoch.layview.gui;

public class WindowPosition {
    int xSize, ySize,
            xOffset, yOffset;

    public WindowPosition(int xSize, int ySize, int xOffset, int yOffset) {
        this.xSize = xSize;
        this.ySize = ySize;
        this.xOffset = xOffset;
        this.yOffset = yOffset;
    }
}
